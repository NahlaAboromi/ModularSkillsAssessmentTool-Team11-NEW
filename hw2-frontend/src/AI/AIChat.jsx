// src/AI/AIChat.jsx
import React, { useState, useRef, useEffect, useContext } from 'react';
import { ThemeContext } from '../DarkLightMood/ThemeContext';
import { LanguageContext } from '../context/LanguageContext';
import { useI18n } from '../utils/i18n';

const AIChat = ({ teacherId }) => {
  const [showBox, setShowBox] = useState(false);
  const [input, setInput] = useState('');
  const { theme } = useContext(ThemeContext);
  const isDark = theme === 'dark';

  const ctx = useContext(LanguageContext) || {};
  const lang = ctx.lang ?? (document.documentElement.lang === 'he' ? 'he' : 'en');
  const isRTL = lang === 'he';
  const { t, ready } = useI18n('aiChat');

  const chatRef = useRef(null);
  const greetingRef = useRef('');
  const [messages, setMessages] = useState([]);
  const [loading, setLoading] = useState(false);

  const toggleChatBox = () => setShowBox(v => !v);

  const closeChat = () => {
    setShowBox(false);
    setMessages([]);
  };

  const scrollToBottom = () => {
    if (chatRef.current) {
      chatRef.current.scrollTop = chatRef.current.scrollHeight;
    }
  };

  useEffect(() => { scrollToBottom(); }, [messages, loading]);

  useEffect(() => {
    if (!showBox || !ready) return;

    const greeting = t('greeting');

    setMessages(prev => {
      if (prev.length === 0) {
        greetingRef.current = greeting;
        return [{ role: 'ai', content: greeting }];
      }

      if (prev[0]?.role === 'ai' && prev[0].content === greeting) {
        return prev;
      }

      if (prev[0]?.role === 'ai') {
        const updated = [...prev];
        updated[0] = { ...updated[0], content: greeting };
        greetingRef.current = greeting;
        return updated;
      }

      return prev;
    });
  }, [showBox, lang, ready, t]);

  const handleSend = async () => {
    if (!input.trim()) return;

    const newMessages = [...messages, { role: 'user', content: input }];
    setMessages(newMessages);
    setInput('');
    setLoading(true);

    try {
      const res = await fetch('/api/claude/chat-insight', {
        method: 'POST',
        headers: { 'Content-Type': 'application/json' },
        body: JSON.stringify({
          teacherId,
          messages: newMessages.map(m => ({
            role: m.role === 'user' ? 'user' : 'assistant',
            content: m.content
          }))
        })
      });

      const data = await res.json().catch(() => ({}));
      const aiReply = data?.success ? data.response : t('fallback');
      setMessages([...newMessages, { role: 'ai', content: aiReply }]);
    } catch {
      setMessages([...newMessages, { role: 'ai', content: t('errGeneric') }]);
    } finally {
      setLoading(false);
    }
  };

  return (
    <>
      <style>{`
        .dot{width:6px;height:6px;margin-inline-end:4px;background-color:currentColor;border-radius:50%;display:inline-block;animation:jump 1.4s infinite ease-in-out both;}
        .dot1{animation-delay:-0.32s;}
        .dot2{animation-delay:-0.16s;}
        .dot3{animation-delay:0;}
        @keyframes jump{0%,80%,100%{transform:translateY(0);}40%{transform:translateY(-6px);}}
        
        /* 驻转专 爪祝 - 专住驻住 */
        .ai-chat-button {
          position: fixed;
          bottom: 1rem;
          background-color: #2563eb;
          color: white;
          padding: 0.75rem;
          border-radius: 9999px;
          box-shadow: 0 10px 15px -3px rgba(0, 0, 0, 0.1);
          transition: background-color 0.2s;
          z-index: 40;
          border: none;
          cursor: pointer;
        }
        
        .ai-chat-button:hover {
          background-color: #1d4ed8;
        }
        
        /* LTR (转) -  转 */
        [dir="ltr"] .ai-chat-button {
          right: 1.5rem;
          left: auto;
        }
        
        /* RTL (注专转) - MOBILE: 专 */
        [dir="rtl"] .ai-chat-button {
          left: 50%;
          transform: translateX(-50%);
          right: auto;
        }
        
        /* RTL (注专转) - TABLET 注: 砖 */
        @media (min-width: 640px) {
          [dir="rtl"] .ai-chat-button {
            left: 1.5rem;
            right: auto;
            transform: none;
          }
        }
        
        /* 转转 爪' - 专住驻住 */
        .ai-chat-box {
          position: fixed;
          display: flex;
          flex-direction: column;
          border-radius: 0.5rem;
          box-shadow: 0 10px 15px -3px rgba(0, 0, 0, 0.1);
          z-index: 50;
        }
        
        /* LTR (转) -  转 */
        [dir="ltr"] .ai-chat-box {
          bottom: 4.5rem;
          right: 0.5rem;
          left: auto;
          width: auto;
          max-height: calc(100vh - 6rem);
        }
        
        @media (min-width: 640px) {
          [dir="ltr"] .ai-chat-box {
            bottom: 6rem;
            right: 1.5rem;
            width: 24rem;
            max-height: 80vh;
          }
        }
        
        @media (min-width: 1024px) {
          [dir="ltr"] .ai-chat-box {
            width: 28rem;
          }
        }
        
        /* RTL (注专转) - MOBILE: 住  */
        [dir="rtl"] .ai-chat-box {
          bottom: 4.5rem;
          left: 0.5rem;
          right: 0.5rem;
          width: auto;
          max-height: calc(100vh - 6rem);
        }
        
        /* RTL (注专转) - TABLET 注: 砖 */
        @media (min-width: 640px) {
          [dir="rtl"] .ai-chat-box {
            left: 1.5rem;
            right: auto;
            width: 24rem;
            max-height: 80vh;
          }
        }
        
        @media (min-width: 1024px) {
          [dir="rtl"] .ai-chat-box {
            width: 28rem;
          }
        }
        
        /* 转专转 - 专住驻住 */
        .ai-chat-header {
          display: flex;
          align-items: center;
          justify-content: space-between;
          padding: 0.75rem 1rem;
        }
        
        .ai-chat-header h4 {
          font-size: 1rem;
          font-weight: 600;
        }
        
        @media (min-width: 640px) {
          .ai-chat-header h4 {
            font-size: 1.125rem;
          }
        }
        
        /* 专 注转 - 专住驻住 */
        .ai-chat-messages {
          flex: 1;
          overflow-y: auto;
          padding: 0.75rem;
        }
        
        @media (min-width: 640px) {
          .ai-chat-messages {
            padding: 1rem;
          }
        }
        
        /* 注转 注转 - 专住驻住 */
        .ai-message-bubble {
          padding: 0.5rem 0.75rem;
          border-radius: 0.375rem;
          font-size: 0.813rem;
          white-space: pre-wrap;
          word-break: break-word;
          max-width: 90%;
        }
        
        @media (min-width: 640px) {
          .ai-message-bubble {
            font-size: 0.875rem;
            max-width: 85%;
          }
        }
        
        @media (min-width: 1024px) {
          .ai-message-bubble {
            font-size: 0.9375rem;
          }
        }
        
        /* 专 拽 - 专住驻住 */
        .ai-chat-input-area {
          display: flex;
          align-items: center;
          padding: 0.75rem;
          gap: 0.5rem;
        }
        
        @media (min-width: 640px) {
          .ai-chat-input-area {
            padding: 0.75rem 1rem;
          }
        }
        
        .ai-chat-input {
          flex: 1;
          border: 1px solid;
          border-radius: 0.375rem;
          padding: 0.5rem;
          font-size: 0.813rem;
        }
        
        @media (min-width: 640px) {
          .ai-chat-input {
            font-size: 0.875rem;
          }
        }
        
        @media (min-width: 1024px) {
          .ai-chat-input {
            font-size: 0.9375rem;
          }
        }
        
        .ai-chat-input:focus {
          outline: none;
          ring: 2px;
          ring-color: #3b82f6;
        }
        
        .ai-chat-send-btn {
          background-color: #2563eb;
          color: white;
          padding: 0.5rem 0.75rem;
          border-radius: 0.375rem;
          font-size: 0.813rem;
          white-space: nowrap;
          transition: background-color 0.2s;
          border: none;
          cursor: pointer;
        }
        
        @media (min-width: 640px) {
          .ai-chat-send-btn {
            padding: 0.5rem 1rem;
            font-size: 0.875rem;
          }
        }
        
        @media (min-width: 1024px) {
          .ai-chat-send-btn {
            font-size: 0.9375rem;
          }
        }
        
        .ai-chat-send-btn:hover:not(:disabled) {
          background-color: #1d4ed8;
        }
        
        .ai-chat-send-btn:disabled {
          opacity: 0.5;
        }
      `}</style>

      {/* 驻转专 爪祝 */}
      <button
        onClick={toggleChatBox}
        className="ai-chat-button"
        title={t('tooltip')}
        aria-label={t('tooltip')}
      >
        
      </button>

      {/* 转转 爪' */}
      {showBox && (
        <div
          key={lang}
          dir={isRTL ? 'rtl' : 'ltr'}
          lang={lang}
          className={`ai-chat-box border ${
            isDark ? 'bg-slate-800 text-white border-slate-700' : 'bg-white text-gray-900 border-gray-300'
          }`}
        >
          {/* 转专转 */}
          <div className={`ai-chat-header border-b ${isDark ? 'border-slate-700' : 'border-gray-300'}`}>
            <h4>{t('title')}</h4>
            <button
              onClick={closeChat}
              className={`text-lg font-bold rounded-full w-8 h-8 flex items-center justify-center transition-colors ${
                isDark ? 'text-pink-400 hover:text-pink-500 bg-slate-700 hover:bg-slate-600'
                       : 'text-red-500 hover:text-red-600 bg-white hover:bg-gray-100'
              }`}
              aria-label="close"
            >
              &times;
            </button>
          </div>

          {/* 注转 */}
          <div ref={chatRef} className="ai-chat-messages space-y-2">
            {messages.map((msg, index) => {
              const isUser = msg.role === 'user';
              return (
                <div
                  key={index}
                  className={`w-full flex ${isUser ? 'justify-end' : 'justify-start'}`}
                  dir={isRTL ? 'rtl' : 'ltr'}
                  lang={lang}
                >
                  <div
                    className={[
                      'ai-message-bubble',
                      isUser
                        ? 'bg-blue-600 text-white'
                        : (isDark ? 'bg-slate-700 text-white' : 'bg-gray-100 text-gray-800'),
                      isRTL ? 'text-right' : 'text-left'
                    ].join(' ')}
                  >
                    <strong><bdi>{isUser ? t('labelYou') : t('labelAI')}:</bdi></strong>{' '}
                    {msg.content}
                  </div>
                </div>
              );
            })}

            {loading && (
              <div className="w-full flex justify-start" dir={isRTL ? 'rtl' : 'ltr'} lang={lang}>
                <div className={[
                  'ai-message-bubble inline-flex items-center gap-1',
                  isDark ? 'bg-slate-700 text-white' : 'bg-gray-100 text-gray-800',
                  isRTL ? 'text-right' : 'text-left'
                ].join(' ')}>
                  <strong><bdi>{t('labelAI')}:</bdi></strong>
                  <div className="flex ms-2">
                    <span className="dot dot1" />
                    <span className="dot dot2" />
                    <span className="dot dot3" />
                  </div>
                </div>
              </div>
            )}
          </div>

          {/* 拽+砖 */}
          <div className={`ai-chat-input-area border-t ${isDark ? 'border-slate-700' : 'border-gray-300'}`}>
            <input
              type="text"
              value={input}
              onChange={(e) => setInput(e.target.value)}
              onKeyDown={(e) => e.key === 'Enter' && handleSend()}
              placeholder={t('placeholder')}
              className={`ai-chat-input focus:outline-none ${
                isDark ? 'bg-slate-700 border-slate-600 text-white placeholder-gray-400' : 'bg-white border-gray-300 text-gray-900 placeholder-gray-500'
              } ${isRTL ? 'text-right' : 'text-left'}`}
              dir={isRTL ? 'rtl' : 'ltr'}
              lang={lang}
            />
            <button
              onClick={handleSend}
              disabled={loading}
              className="ai-chat-send-btn"
            >
              {loading ? t('asking') : t('ask')}
            </button>
          </div>
        </div>
      )}
    </>
  );
};

export default AIChat;